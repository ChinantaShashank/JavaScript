//For Loop
let a=["Fool"," "," ","Fool"," "," "," Fool",,];
i=0;
for(i=0;i<10;i++)
{   if(a[i]=="Fool")
    console.log("😂 ");
    else
    console.log("😎 ");
}
console.log("\n");
//While Loop
i=0;
while(i<10)
{
    if(a[i]=="Fool")
    console.log("😥");
    else
    console.log("👌");
    i++;
}
console.log("\n");
//Do While Loop
i=0;
do{
    if(a[i]=="Fool")
    console.log("❤️");
    else
    console.log("🔥");
    i++;
}
while(i<10);