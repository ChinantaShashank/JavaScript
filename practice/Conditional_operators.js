//If else Statements
let Rs=100;
if(Rs>=50)
{
    console.log("You are having more than 50 rupees 👍");
}
else
console.log("You should work hard to earn more 😂");

//Switch Case Statements
let day=4;
switch(day)
{
    case 1: console.log("Monday");
            break;
    case 2: console.log("Tuesday");
            break;
    case 3:console.log("Wednesday");
            break;
    case 4:console.log("Thursday");
            break;
    case 5:console.log("Friday");
            break;
    case 6:console.log("Saturday");
            break;
    case 7:console.log("Sunday");
            break;
    default:console.log("Number is not in 1-7 range");
}

//Ternary Operators
let rain=true;
rain? console.log("It's raining 💧") : console.log("It's not raining");